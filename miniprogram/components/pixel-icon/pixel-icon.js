/**
 * 像素图标组件
 * @description 渲染8x8像素网格的像素风格图标
 */
Component({
    properties: {
        name: {
            type: String,
            value: 'app',
        },
        size: {
            type: Number,
            value: 48,
        },
    },

    data: {
        pixels: [],
        pixelSize: 3,
        containerSize: 48,
    },

    observers: {
        'name,size': function () {
            this.build();
        },
    },

    lifetimes: {
        attached() {
            this.build();
        },
    },

    methods: {
        /**
         * 构建像素图标
         * @returns {void}
         */
        build() {
            const size = Number(this.data.size) || 48;
            const pixelSize = Math.max(2, Math.round(size / 8));
            const containerSize = pixelSize * 8;
            const iconColorMap = {
                home: '#2d2d2d',
                back: '#2d2d2d',
                app: '#6ea85e',
                music: '#a77bd8',
                web: '#4e63ad',
                nfc: '#2d2d2d',
                phone: '#2d2d2d',
                android: '#6ea85e',
                check: '#6ea85e',
                error: '#be4a3a',
                question: '#2d2d2d',
                heart: '#e86a58',
                user: '#2d2d2d',
                globe: '#4e63ad',
                note: '#a77bd8',
                record: '#d79143',
            };
            const iconColor = iconColorMap[this.data.name] || '#2d2d2d';

            const palette = {
                '0': 'transparent',
                '1': iconColor,
            };

            const icon = this.getIcon(this.data.name);
            const pixels = [];
            for (let y = 0; y < 8; y++) {
                const row = icon[y] || '';
                for (let x = 0; x < 8; x++) {
                    const key = row[x] || '0';
                    pixels.push(palette[key] || palette['0']);
                }
            }

            this.setData({
                pixels,
                pixelSize,
                containerSize,
            });
        },

        /**
         * 获取图标数据
         * @param {string} name - 图标名称
         * @returns {string[]} 图标像素数据
         */
        getIcon(name) {
            const icons = {
                home: ['00111000', '01111100', '11111110', '11111110', '11011010', '11000010', '11111110', '00000000'],
                back: ['00011000', '00111000', '01111000', '11111111', '11111111', '01111000', '00111000', '00011000'],
                app: ['01111110', '11000011', '10111101', '10100101', '10111101', '10100101', '11000011', '01111110'],
                music: ['00011110', '00011110', '00010010', '00010010', '00110010', '01110000', '11100000', '01100000'],
                web: ['00111100', '01111110', '11100111', '11111111', '11111111', '11100111', '01111110', '00111100'],
                nfc: ['01111110', '11000011', '10111101', '10100101', '10111101', '10100101', '11000011', '01111110'],
                phone: ['00111100', '01111110', '01000010', '01000010', '01000010', '01000010', '01111110', '00111100'],
                android: ['00111100', '01111110', '11011011', '11111111', '11111111', '10111101', '10100101', '00000000'],
                check: ['00111100', '01111110', '11101111', '11111111', '11111111', '11111110', '01111100', '00111000'],
                error: ['11000011', '01100110', '00111100', '00011000', '00011000', '00111100', '01100110', '11000011'],
                question: ['00111100', '01111110', '01111110', '01100110', '00100100', '00100100', '00011000', '00000000'],
                heart: ['01100110', '11111111', '11111111', '11111111', '01111110', '00111100', '00011000', '00000000'],
                user: ['00111100', '01111110', '01111110', '00111100', '00111100', '01111110', '11111111', '00000000'],
                globe: ['00111100', '01111110', '11100111', '11111111', '11111111', '11100111', '01111110', '00111100'],
                note: ['00011110', '00011110', '00010010', '00010010', '00110010', '01110000', '11100000', '01100000'],
                record: ['00111100', '01111110', '11111111', '11100111', '11100111', '11111111', '01111110', '00111100'],
            };

            return icons[name] || icons.app;
        },
    },
});
